package BascisOfJava;

public class DataTypeDecleration {

	public static void main(String[] args) {
		// Data Type Declaration
		
		// Integer Data Types
		byte b = 10;	// 1 Bytes - 8 bits
		short s = 100;	// 2 Bytes - 16 bits
		int i = 1000;	//  4 Bytes - 32 bits
		long l = 1024;	// 8 Bytes - 64 bits
		
		// Decimal Integer
		float f = 10.0f;	//  4 Bytes - 32 bits
		double d = 10.5;	// 8 Bytes - 64 bits
		
		// Character Data Types
		char ch = 'a';			// 2 Bytes - 16 bits
		boolean bool = true;	// 1 bit - true or false
		
		//Printing data types.
		System.out.println(b);
		System.out.println(s);
		System.out.println(i);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(ch);
		System.out.println(bool);
		
	}

}
